            'menus' => Menu::myMenu()
        ];
        return view('menu.index', $data);
    }

    public function statusMenu(Request $req)
    {
        $menu = Menu::find($req['id']);
        $menu->status = $req['status'];
        $menu->save();
        return redirect(route('menu.index'));
    }

